#!/usr/bin/env python3
"""
Prophet Forecast Service - Entry Point

Run this script to start the Prophet forecast service.
"""

import uvicorn

if __name__ == "__main__":
    uvicorn.run(
        "app.main:app",
        host="0.0.0.0",
        port=8000,
        reload=True,
        log_level="info"
    )
